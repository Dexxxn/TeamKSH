package com.ksh.model;

public class PatientVO {
	private String p_name;
	private String p_no;
	private String p_addr;
	private String p_phone;
	private String p_visit;
	private String p_content;
	private String p_chart;

	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_no() {
		return p_no;
	}
	public void setP_no(String p_no) {
		this.p_no = p_no;
	}
	public String getP_addr() {
		return p_addr;
	}
	public void setP_addr(String p_addr) {
		this.p_addr = p_addr;
	}
	public String getP_phone() {
		return p_phone;
	}
	public void setP_phone(String p_phone) {
		this.p_phone = p_phone;
	}
	public String getP_visit() {
		return p_visit;
	}
	public void setP_visit(String p_visit) {
		this.p_visit = p_visit;
	}
	public String getP_content() {
		return p_content;
	}
	public void setP_content(String p_content) {
		this.p_content = p_content;
	}
	public String getP_chart() {
		return p_chart;
	}
	public void setP_chart(String p_chart) {
		this.p_chart = p_chart;
	}
	
}
